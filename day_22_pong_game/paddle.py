from turtle import Turtle

class Paddle(Turtle):
    def __init__(self, init_pos: tuple):
        super().__init__()
        self.shape("square")
        self.color("white")
        self.shapesize(stretch_wid=5, stretch_len=1)
        self.speed(0)
        self.penup()
        self.goto(init_pos)

    def move_up(self):
        self.goto(self.xcor(), self.ycor() + 20)

    def move_down(self):
        self.goto(self.xcor(), self.ycor() - 20)
